    public void build_InitializationTable(){
          String[] titles = {"No spreadsheet imported yet."};       
          TableVideoModel=new DefaultTableModel(null, titles){
                @Override
                 public boolean isCellEditable(int row1, int column) {   
                     if(column == 2){
                         return true;
                     }
                     return false;
                 }
                 private static final long serialVersionUID = 1L;  
                @Override
                public Class<?> getColumnClass(int column) {  
                    switch (column) {  
                        case 0:
                            return String.class;
                        case 1:  
                            return String.class;
                        case 2:  
                            return Boolean.class;
                        case 3:  
                            return String.class; 
                        default:
                            return   String.class;
                    }  
                } 
           };
        try{  
            TableVideoModel.addRow(new Object[]{"               "
                    + "Please, import a spreadsheet with the video data."});            
         }catch(Exception e){
             System.err.println(e);
        }                 
        
        //Setting colors and size
        VideoTable.setRowHeight(150);
        VideoTable.setModel(TableVideoModel);
        VideoTable.setFont(new Font("Dialog", Font.ITALIC, 20));   
        VideoTable.getTableHeader().setFont(new Font("Dialog", Font.BOLD, 20));
        
        VideoTable.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                VideoTableMouseClicked(evt);
            }

            private void VideoTableMouseClicked(MouseEvent evt) {
                loadInfoFromSelectedRow();       
            }
        });
        
        VideoTable.addKeyListener(new java.awt.event.KeyAdapter() {
            public void keyPressed(java.awt.event.KeyEvent evt) {
                VideoTableKeyPressed(evt);
            }
            
            public void VideoTableKeyPressed(KeyEvent evt){                
                if(evt.getKeyCode() == 38 || evt.getKeyCode() == 40){
                    loadInfoFromSelectedRow();
                }
            }
        });
        
        TableColumn tm = VideoTable.getColumnModel().getColumn(0);
        tm.setCellRenderer(new ColorColumnRenderer(new Color(191,191,191), new Color(0, 38, 77)));
    }
    
    public void loadInfoFromSelectedRow(){        
        String videoName = VideoTable.getValueAt(VideoTable.getSelectedRow(), 0).toString();
        selectedTableItem = videoName;

        for(Video video: videos){
            if(video.getName().equals(videoName)){                      
              load_desiredProperties(video.getName(), video.getAuthor_url(), 
                               video.getComments(), video.getPromotion_url(),
                               video.getRating(), video.getSubtitles(), 
                               video.getTags(), video.getTitle());                                                        
              
              if(video.getVideoMetadata()!=null){                  
                VideoMetadata metadata = video.getVideoMetadata();               
                load_currentProperties(metadata.getName(), metadata.getAuthor_url(), metadata.getComments(),
                                      metadata.getPromotion_url(), metadata.getRating(), metadata.getSubtitles(), 
                                      metadata.getTags(), metadata.getTitle());
              }
            }
        }   
    }
    
