    private void jFileChooser1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFileChooser1ActionPerformed
              
        if(jFileChooser1.getFileSelectionMode() == 0){     
            jButton4.setEnabled(false);
            jMenuItem3.setEnabled(false);
            File spreadsheet = jFileChooser1.getSelectedFile();
            FileChooserWindow.dispose();

            // Gets the format of the file selected
            String fileAbsolutePath=spreadsheet.getName();
            spreadsheet.getAbsolutePath();
            String fileExtension = fileAbsolutePath.substring(fileAbsolutePath.lastIndexOf(".") + 1, fileAbsolutePath.length());                         

            try{
                ReadWriteExcelFile decoder = new ReadWriteExcelFile();

                if(fileExtension.equals("xls")){                           
                    decoder.readXLSFile(spreadsheet.toString());
                }else if(fileExtension.equals("xlsx")){            
                    decoder.readXLSXFile(spreadsheet.toString());
                }
            }catch(Exception e){
                System.err.println("Error getting files in main.java "+e);
                e.printStackTrace();
            }   
        }else{
            FileChooserWindow.dispose();
            videoDirectorySourcePath = jFileChooser1.getSelectedFile().toString();            
            
            //If all went successfully
            if(TableService.SetDefaultTableStyle()){
              //Fills the fields with the desired properties
               selectedTableItem = videos.get(0).getName();
               load_desiredProperties(videos.get(0).getName(), videos.get(0).getAuthor_url(), 
                                      videos.get(0).getComments(), videos.get(0).getPromotion_url(), 
                                      videos.get(0).getRating(), videos.get(0).getSubtitles(), 
                                      videos.get(0).getTags(), videos.get(0).getTitle());
            }                        
        }
    }//GEN-LAST:event_jFileChooser1ActionPerformed

    public void load_desiredProperties(String name, String authorUrl, String comments,
            String promotionUrl, String rating, String subtitles, String tags, String title){
               
        newName.setText(name);
        newAuthor_URL.setText(authorUrl);
        newComments.setText(comments);
        newPromotion_URL.setText(promotionUrl);
        Double v = Double.parseDouble(rating);         
        newRating.setValue(v * 1);
        newSubtitles.setText(subtitles);
        newTags.setText(tags);
        newTitle.setText(title);
    }
    
